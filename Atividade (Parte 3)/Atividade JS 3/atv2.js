//2) Crie um programa que solicite ao usuário um valor em horas e converta para minutos e segundos.

let valorHoras = Number(prompt("Digite o valor em horas: "))

valorMinutos = valorHoras * 60
valorSegundos = valorMinutos * 60

alert ("O valor em horas é: " + valorHoras)
alert ("O valor em minutos é: " + valorMinutos)
alert ("O valor em segundos é: " + valorSegundos)